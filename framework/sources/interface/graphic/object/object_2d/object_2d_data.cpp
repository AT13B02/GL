//=============================================================================
// コンストラクタ
//=============================================================================
CObject2DData::CObject2DData(void)
{
	object_2d_ = NULL;
	texture_name_ = "";
	position_ = VECTOR2(0.0f,0.0f);
	scale_ = VECTOR2(1.0f,1.0f);
	rotation_ = 0.0f;
	renderstate_ = NULL;
	matrix_.SetIdentity();
}

//=============================================================================
// デストラクタ
//=============================================================================
CObject2DData::~CObject2DData(void)
{
}

//=============================================================================
// 初期化処理
//=============================================================================
bool CObject2DData::Init(void)
{
	return true;
}

//=============================================================================
// 描画処理
//=============================================================================
void CObject2DData::Draw(void)
{
	if(object_2d_ != NULL)
	{
		// 描画
		object_2d_->Draw(this);
	}
}

//=============================================================================
// 終了処理
//=============================================================================
void CObject2DData::Uninit(void)
{
}

