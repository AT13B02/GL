//=============================================================================
// コンストラクタ
//=============================================================================
CObject2DData::CObject2DData(void)
{
	m_pObject2D = NULL;
	m_pTexture = NULL;
	m_Pos = VECTOR2(0.0f,0.0f);
	m_Scale = VECTOR2(1.0f,1.0f);
	m_fRot = 0.0f;
	m_pRenderstate = NULL;
	m_Matrix.SetIdentity();

}

//=============================================================================
// デストラクタ
//=============================================================================
CObject2DData::~CObject2DData(void)
{
}

//=============================================================================
// 初期化処理
//=============================================================================
bool CObject2DData::Init(void)
{
	return true;
}

//=============================================================================
// 描画処理
//=============================================================================
void CObject2DData::Draw(void)
{
	if(m_pObject2D != NULL)
	{
		// 描画
		m_pObject2D->Draw(this);
	}
}

//=============================================================================
// 終了処理
//=============================================================================
void CObject2DData::Uninit(void)
{
}

