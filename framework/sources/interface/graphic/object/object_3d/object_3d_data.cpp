//=============================================================================
// コンストラクタ
//=============================================================================
CObject3DData::CObject3DData(void)
{
	object_3d_ = NULL;
	texture_name_ = "";
	position_ = VECTOR3(0.0f,0.0f,0.0f);
	scale_ = VECTOR3(1.0f,1.0f,1.0f);
	rotation_ = VECTOR3(0.0f,0.0f,0.0f);
	matrix_.SetIdentity();
}

//=============================================================================
// デストラクタ
//=============================================================================
CObject3DData::~CObject3DData(void)
{
}

//=============================================================================
// 初期化処理
//=============================================================================
bool CObject3DData::Init(void)
{
	return true;
}

//=============================================================================
// 描画処理
//=============================================================================
void CObject3DData::Draw(void)
{
	if(object_3d_ != NULL)
	{
		// 描画
		object_3d_->Draw(this);
	}
}

