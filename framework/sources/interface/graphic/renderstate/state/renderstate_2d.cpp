{
}

//=============================================================================
// 作成処理
//=============================================================================
CRenderstate2D* CRenderstate2D::Create(CDeviceHolder* device_holder)
{
	CRenderstate2D* renderstate_2d = NULL;

#ifdef _USING_OPENGL_
	renderstate_2d = new CGLRenderstate2D(device_holder);
#endif

#ifdef _USING_DIRECTX_
	renderstate_2d = new CDXRenderstate2D(device_holder);
#endif

	return renderstate_2d;
}

