//=============================================================================
// 作成処理
//=============================================================================
CRenderstate3D* CRenderstate3D::Create(CDeviceHolder* device_holder)
{
	CRenderstate3D* renderstate_3d = NULL;

#ifdef _USING_OPENGL_
	renderstate_3d = new CGLRenderstate3D(device_holder);
#endif

#ifdef _USING_DIRECTX_
	renderstate_3d = new CDXRenderstate3D(device_holder);
#endif

	return renderstate_3d;
}

