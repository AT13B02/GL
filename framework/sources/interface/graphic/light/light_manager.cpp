{
}

//=============================================================================
// 終了処理
//=============================================================================
void CLightManager::Uninit(void)
{
	for(auto it = light_list_.begin();it != light_list_.end();++it)
	{
		(*it)->Uninit();
	}

	light_list_.clear();
}

//=============================================================================
// 設定処理
//=============================================================================
void CLightManager::Set(void)
{
	// ライトの設定
	for(auto it = light_list_.begin();it != light_list_.end();++it)
	{
		(*it)->Set();
	}
}

//=============================================================================
// 設定処理
//=============================================================================
void CLightManager::Add(CLight* light)
{
	// ライトの追加
	light_list_.push_back(light);
}

