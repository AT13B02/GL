//=============================================================================
// デフォルトコンストラクタ
//=============================================================================
CTexture::CTexture(CDeviceHolder* device_holder)
{
	filename_ = NULL;

	// グラフィックデバイスの設定
	device_holder_ = device_holder;
}

//=============================================================================
// デストラクタ
//=============================================================================
CTexture::~CTexture(void)
{
}

//=============================================================================
// ロード処理
//=============================================================================
bool CTexture::Init(void)
{
	filename_ = NULL;

	return true;
}

//=============================================================================
// 終了処理
//=============================================================================
void CTexture::Uninit(void)
{
	SAFE_DELETE_ARRAY(filename_);
}

//=============================================================================
// ファイル名の設定処理
//=============================================================================
void CTexture::SetFilename(const char* filename)
{
	int nLen = 0;

	// ファイル名の長さの取得
	nLen = strlen(filename);

	// ファイル名分確保
	filename_ = new char[nLen + 1];

	// ファイル名のコピー
	strcpy(filename_,filename);
}

