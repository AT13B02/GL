#include "interface/input/device/input_device.h"
#include "interface/input/event_buffer.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CInputDevice::CInputDevice(CEventBuffer* event_buffer)
{
	event_buffer_ = event_buffer;
}

//=============================================================================
// デストラクタ
//=============================================================================
CInputDevice::~CInputDevice(void)
{
}

//=============================================================================
// 初期化
//=============================================================================
bool CInputDevice::Init(void)
{
	return true;
}

//=============================================================================
// 終了
//=============================================================================
void CInputDevice::Uninit(void)
{
	// デバイスの開放
	if(direct_input_device_ != NULL)
	{
		direct_input_device_->Unacquire();
		direct_input_device_->Release();
		direct_input_device_ = NULL;
	}
}

