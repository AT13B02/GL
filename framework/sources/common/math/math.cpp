//=============================================================================
// 2次元ベクトルの2点間の距離を取得
//=============================================================================
f32 GetVector2Length(const VECTOR2& vector1,const VECTOR2& vector2)
{
	f32 length = 0.0f;
	VECTOR2 work;

	work = vector2 - vector1;

	length = work.Magnitude();

	return length;
}

//=============================================================================
// 3次元ベクトルの2点間の距離を取得
//=============================================================================
f32 GetVector3Length(const VECTOR3& vector1,const VECTOR3& vector2)
{
	f32 length = 0.0f;
	VECTOR3 work;

	work = vector2 - vector1;

	length = work.Magnitude();

	return length;
}

